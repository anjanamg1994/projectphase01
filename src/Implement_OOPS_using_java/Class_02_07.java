package Implement_OOPS_using_java;

public class Class_02_07 {
	public static void main(String[] args){
	
		int x= 10, y = 20;
		System.out.println( " x value is  "+ x);
		System.out.println("y value is "+ y);
		System.out.println(x + ">"+ y +" is : " + (x>y));
		System.out.println(x + "<"+ y +" is : " + (x<y));
		System.out.println(x + ">= "+ y +"is : " + (x>=y));
		System.out.println(x + " <= "+ y +"is : " + (x<=y));
		System.out.println(x + "!="+ y +"is : " + (x!=y));
		System.out.println(x + "=="+ y +"is : " + (x==y));
	}
}
