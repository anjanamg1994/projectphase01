package self_learning_class;

public class Unary_operators_Arthimetic_Operators_01 {
	public static void main(String args[]) {
		// Unary operatoprs and arthimetic operators 
		int a = 50;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
		// Arthimetic operatos
		int b = 10, c =5; 
		System.out.println(b+c);
		System.out.println(b-c);
		System.out.println(b*c);
		System.out.println(b/c);
		System.out.println(b%c);
		
	}

}
